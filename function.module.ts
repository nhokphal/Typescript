export const Add = (a: number, b: number): number => {
  return a + b;
};

export const Minus = (a: number, b: number): number => {
  //   return result;
  return a - b;
};

export const Multiply = (a: number, b: number): number => {
  return a * b;
};

export const Divide = (a: number, b: number): number => {
  return a / b;
};





